public class EstructuraDoWhile {

    public static void main(String[] args) {

        int contador = 1;

        do {
            System.out.println("contador = " + contador);
            contador++;
        } while (contador<=10);

        //Ejercicio
        int num = 1;

        do{
            System.out.println("Numero = " + num);
            num++;
        } while (num<=15);
    }
}
